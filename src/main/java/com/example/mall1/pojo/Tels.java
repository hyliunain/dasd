package com.sample;


public class Tels {

  private long id;
  private String phone;
  private long eno;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getPhone() {
    return phone;
  }

  public void setPhone(String phone) {
    this.phone = phone;
  }


  public long getEno() {
    return eno;
  }

  public void setEno(long eno) {
    this.eno = eno;
  }

}
