package com.sample;


public class Dept {

  private long dno;
  private String dname;
  private String tel;


  public long getDno() {
    return dno;
  }

  public void setDno(long dno) {
    this.dno = dno;
  }


  public String getDname() {
    return dname;
  }

  public void setDname(String dname) {
    this.dname = dname;
  }


  public String getTel() {
    return tel;
  }

  public void setTel(String tel) {
    this.tel = tel;
  }

}
